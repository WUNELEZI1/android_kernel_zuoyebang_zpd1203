static kal_uint8 ct_read_data[CROSSTALK_All_SIZE];
static kal_uint8 ct_read_new_data[CROSSTALK_BUF_SIZE];

void gc50f6_read_crosstalk_data(void)
{
	kal_uint16 i = 0;
	kal_uint16 j = 0;
	kal_uint16 ct_flag = 0;
	kal_uint32 checksum = 0;
	pr_debug("start read crosstalk");
	memset(&ct_read_data, 0, CROSSTALK_All_SIZE * sizeof(kal_uint8));
	memset(&ct_read_new_data, 0, CROSSTALK_BUF_SIZE * sizeof(kal_uint8));

	ct_flag = read_cmos_sensor_gc50f6_ofilm_otp(CROSSTALK_FLAG_OFFSET);
	pr_debug("ct_flag = %d\n",ct_flag);
//  ct_flag = 1;
    if (ct_flag == OTP_FLAG_VALID) {
		for (i = 0; i < CROSSTALK_All_SIZE; i++)
			ct_read_data[i] =read_cmos_sensor_gc50f6_ofilm_otp(CROSSTALK_START_ADDR + i);

		for (i=0; i < (CROSSTALK_All_SIZE  - 1); i++)
			checksum += ct_read_data[i];

		//checksum++;
		pr_debug("calc_checksum =%d, read_checksum = %d\n",
		    (checksum % 255)+1, ct_read_data[CROSSTALK_All_SIZE -1]);

		if (((checksum % 255)+1) == ct_read_data[CROSSTALK_All_SIZE-1])
			pr_debug("check success! calc_checksum =%d, read_checksum = %d\n",
		    (checksum % 255)+1, ct_read_data[CROSSTALK_All_SIZE -1]);
		else {
			memset(ct_read_data, 0, CROSSTALK_All_SIZE);
			pr_debug("check error! calc_checksum =%d, read_checksum = %d\n",
		    ((checksum % 255)+1), ct_read_data[CROSSTALK_All_SIZE -1]);
		}

#if CT_DEBUG
        for (i = 0; i< CROSSTALK_All_SIZE; i++)
			pr_debug("ct_data[%d] = %x\n", i, ct_read_data[i]);
#endif
	} else
		pr_debug("crosstalk data is zero!\n");

    for (i = 0; i < CROSSTALK_All_SIZE; i++) {
        if (i < 5)     continue;             // 跳过前5个元素
        if (i == 299 || i == 300)  continue; // 跳过第298和299个元素

        ct_read_new_data[j] = ct_read_data[i];

	 // pr_debug("ct_read_new_data[%d] = %x\n", j, ct_read_new_data[j]);
		j++;
    }

}

void gc50f6_write_crosstalk_data(void)
{
	kal_uint32 i = 0, page = 0, j = 0;
	kal_uint32 start_page = 0x06, start_addr = 0x0600;

	write_cmos_sensor_8bit(0x031c, 0x60);
	write_cmos_sensor_8bit(0x0004, 0x06);
	write_cmos_sensor_8bit(0x001f, 0x01);
	mdelay(5);
	pr_debug("start write crosstalk_data!\n");

	for (page = 0; page < 2; page++)
	{
		write_cmos_sensor_8bit(0x0004, start_page+0x10*page);
		for (i = 0; i < 128 && j < CROSSTALK_BUF_SIZE - 1; i++)
		{
			write_cmos_sensor(start_addr+2*i, ((ct_read_new_data[j] << 8) & 0xFF00)|ct_read_new_data[j+1]);
			j=j+2;
			//pr_debug("XTCdata = 0x%x\n",read_cmos_sensor(start_addr+2*i));
			//pr_debug("XTCdata = 0x%x\n",read_cmos_sensor(start_addr+2*i+1));
		}
	pr_debug("page = %x\n", read_cmos_sensor(0x0004));

	}

	write_cmos_sensor_8bit(0x0004, 0x07);
	write_cmos_sensor_8bit(0x031c, 0x9b);

	// pr_debug("sram_i = %d\n", i);
   // pr_debug("CTdata_j = %d\n", j);
	pr_debug("end write crosstalk_data!\n");

}


/*************************************************************************
 * FUNCTION
 *	get_imgsensor_id
 *
 * DESCRIPTION
 *	This function get the sensor ID
 *
 * PARAMETERS
 *	*sensorID : return the sensor ID
 *
 * RETURNS
 *	None
 *
 * GLOBALS AFFECTED
 *
 *************************************************************************/
static kal_uint32 get_imgsensor_id(UINT32 *sensor_id)
{
	kal_uint8 i = 0;
	kal_uint8 retry = 2;

	while (imgsensor_info.i2c_addr_table[i] != 0xff) {
		spin_lock(&imgsensor_drv_lock);
		imgsensor.i2c_write_id = imgsensor_info.i2c_addr_table[i];
		spin_unlock(&imgsensor_drv_lock);
		do {
			*sensor_id = return_sensor_id();
			if (*sensor_id == imgsensor_info.sensor_id) {
				pr_debug("[gc50f6_camera_sensor]get_imgsensor_id:i2c write id: 0x%x, sensor id: 0x%x\n",
					imgsensor.i2c_write_id, *sensor_id);
#if 1
					gc50f6_ofilm_fusion_id_read();
					gc50f6_ofilm_sn_id_read();
#endif
					gc50f6_read_crosstalk_data();
				return ERROR_NONE;
			}
			pr_debug("[gc50f6_camera_sensor]get_imgsensor_id:Read sensor id fail, write id: 0x%x, id: 0x%x\n",
				imgsensor.i2c_write_id, *sensor_id);
			retry--;
		} while (retry > 0);
		i++;
		retry = 2;
	}
	if (*sensor_id != imgsensor_info.sensor_id) {
		/* if Sensor ID is not correct,
		 * Must set *sensor_id to 0xFFFFFFFF
		 */
		*sensor_id = 0xFFFFFFFF;
		return ERROR_SENSOR_CONNECT_FAIL;
	}
	return ERROR_NONE;
}


/*************************************************************************
 * FUNCTION
 *	open
 *
 * DESCRIPTION
 *	This function initialize the registers of CMOS sensor
 *
 * PARAMETERS
 *	None
 *
 * RETURNS
 *	None
 *
 * GLOBALS AFFECTED
 *
 *************************************************************************/
static kal_uint32 open(void)
{
	kal_uint8 i = 0;
	kal_uint8 retry = 2;
	kal_uint16 sensor_id = 0;

	pr_debug("%s +\n", __func__);

	while (imgsensor_info.i2c_addr_table[i] != 0xff) {
		spin_lock(&imgsensor_drv_lock);
		imgsensor.i2c_write_id = imgsensor_info.i2c_addr_table[i];
		spin_unlock(&imgsensor_drv_lock);
		do {
			sensor_id = return_sensor_id();
			if (sensor_id == imgsensor_info.sensor_id) {
				pr_debug("[gc50f6_camera_sensor]open:i2c write id: 0x%x, sensor id: 0x%x\n",
					imgsensor.i2c_write_id, sensor_id);
				break;
			}
			pr_debug("[gc50f6_camera_sensor]open:Read sensor id fail, write id: 0x%x, id: 0x%x\n",
				imgsensor.i2c_write_id, sensor_id);
			retry--;
		} while (retry > 0);
		i++;
		if (sensor_id == imgsensor_info.sensor_id)
			break;
		retry = 2;
	}
	if (imgsensor_info.sensor_id != sensor_id)
		return ERROR_SENSOR_CONNECT_FAIL;

	/* initail sequence write in  */
	sensor_init();

	gc50f6_write_crosstalk_data();

	spin_lock(&imgsensor_drv_lock);

	imgsensor.autoflicker_en = KAL_FALSE;
	imgsensor.sensor_mode = IMGSENSOR_MODE_INIT;
	imgsensor.shutter = 0x480;
	imgsensor.gain = 0x40;
	imgsensor.pclk = imgsensor_info.pre.pclk;
	imgsensor.frame_length = imgsensor_info.pre.framelength;
	imgsensor.line_length = imgsensor_info.pre.linelength;
	imgsensor.min_frame_length = imgsensor_info.pre.framelength;
	imgsensor.dummy_pixel = 0;
	imgsensor.dummy_line = 0;
	imgsensor.ihdr_mode = 0;
	imgsensor.test_pattern = KAL_FALSE;
	imgsensor.current_fps = imgsensor_info.pre.max_framerate;
	spin_unlock(&imgsensor_drv_lock);
	pr_debug("%s -\n", __func__);

	return ERROR_NONE;
}

